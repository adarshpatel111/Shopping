import Mongoose from "mongoose";

const userSchema = new Mongoose.Schema({
    firstName: {
        type: String,
        required: true,
        trim: true
    },
    lastName: {
        type: String,
        required: true,
        trim: true
    },
    email: {
        type: String,
        required: true,
        unique: true,
        trim: true,
        lowercase: true
    },
    password: {
        type: String,
        required: true
    }
})
//Create model for export schema
const User = Mongoose.model('User', userSchema);

export default User